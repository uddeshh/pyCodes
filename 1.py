A=list(map(int,input().split(',')))
B=int(input())
N=len(A)
print(N,B)
if N>=2*B:
    new=A
else:
    new=A[:B]+A[N-B:]
new=new+new
print(new)
mx=0
for i in range(len(new)):
    tot=sum(new[i:i+B])
    print(tot)
    if tot>mx:
        mx=tot
print(mx)
